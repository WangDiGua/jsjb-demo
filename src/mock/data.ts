export function enrichDepartmentsFromAppeals(
  catalog: DepartmentCatalogEntry[],
  appeals: Appeal[],
): Department[] {
  return catalog.map((t) => {
    const mine = appeals.filter((a) => a.departmentId === t.id);
    const 受理数 = mine.length;
    const 答复数 = mine.filter((a) => a.status === 'replied').length;
    const ratings = mine.filter((a) => a.评价?.rating != null).map((a) => a.评价!.rating);
    const 评分 =
      ratings.length > 0
        ? Math.min(5, Math.round((ratings.reduce((s, r) => s + r, 0) / ratings.length) * 10) / 10)
        : 4.6;
    return { ...t, 受理数, 答复数, 评分 };
  });
}

