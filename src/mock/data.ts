export function deriveQuestionTypeCounts(appeals: Appeal[]): QuestionType[] {
  return questionTypeDefinitions
    .map((t) => ({
      ...t,
      count: appeals.filter((a) => a.type === t.name).length,
    }))
    .sort((a, b) => b.count - a.count || a.order - b.order);
}

